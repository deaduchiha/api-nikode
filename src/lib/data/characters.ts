export const getCharacterById = (id: string): Character | undefined => {
  return characters.find((character) => character.id === id);
};

export const searchCharacters = (query: string): Character[] => {
  const lowercaseQuery = query.toLowerCase();
  return characters.filter(
    (character) =>
      character.name.toLowerCase().includes(lowercaseQuery) ||
      character.anime.toLowerCase().includes(lowercaseQuery) ||
      character.description.toLowerCase().includes(lowercaseQuery)
  );
};

export const filterCharactersByAnime = (anime: string): Character[] => {
  return characters.filter(
    (character) => character.anime.toLowerCase() === anime.toLowerCase()
  );
};

export const getCharactersByPowerRange = (
  minPower: number,
  maxPower: number
): Character[] => {
  return characters.filter(
    (character) => character.power >= minPower && character.power <= maxPower
  );
};
